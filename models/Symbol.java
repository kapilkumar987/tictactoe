package models;

public class Symbol
{
    private char s; // attributes | member variables of the class
    public Symbol(char s) // Parameterized constructor
    {
        this.s = s;
    }

    // Getters and Setters

    public char getS()
    {
        return this.s;
    }

    public void setS(char s)
    {
        this.s = s;
    }
}
